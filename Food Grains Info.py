"""
Write a code which takes the names of the food grains for e.g. Rice, Wheat,
Blackgram etc. and display information about that foodgrain.

"""

print("Please choose any one Food Grain :- ")
print("1. Rice")
print("2. Wheat ")
print("3. Blackgram")


choice = (input("Enter your choice: "))


if choice== "Rice":
    print("Rice, (Oryza sativa), edible starchy cereal grain and the grass plant (family Poaceae) by which it is produced")
    
if choice=="Wheat":
    print("Wheat is a grass widely cultivated for its seed, a cereal grain which is a worldwide staple food.")    
    
if choice=="Blackgram":
    print("Black gram (Vigna mungo (L.) Hepper) is an erect, fast-growing annual, herbaceous legume reaching 30-100 cm in height.")
    