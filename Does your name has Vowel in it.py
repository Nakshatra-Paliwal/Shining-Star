"""
Create a program which checks whether there are any vowels 
(a,e,i,o,u) in the name entered by the user.

"""
vowels = input("Enter your Name: ")

if "a" or "e" or "i" or "o" or "u" in vowels:
    print("You have Vowels in your Name!!")
else:
    print("You do not have Vowels in your Name!!")
    
    
    
